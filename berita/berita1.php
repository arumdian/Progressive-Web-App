            <h1>Istana: Ketua KPK Firli Bahuri Harus Lepas Jabatan di Polri</h1>
            <div class="single_page_content"> <img class="img-center" src="../pages/images/berita1.jpg" alt="" style="width:500px;height:300px;">
              <p>Staf Khusus Presiden Bidang Hukum Dini Shanti Purwono mengatakan Ketua Komisi Pemberantasan Korupsi (KPK) Firli Bahuri harus nonaktif dan melepas jabatannya di Polri. Hal ini berdasarkan ketentuan Pasal 29 Undang-Undang Nomor 19 Tahun 2019 tentang KPK.</p>
			  <p>"Pasal 29 UU KPK jelas menyatakan bahwa pimpinan KPK harus melepaskan semua jabatan selama menjabat sebagai pimpinan KPK. Jadi harus nonaktif dari jabatan lain selama menjabat sebagai Pimpinan KPK," kata Dini saat dikonfirmasi, Selasa (24/12).</p>
			  <p>Untuk diketahui, usai menjabat Kepala Badan Pemeliharaan Keamanan (Baharkam) Polri, Firli kini menjadi analisis kebijakan Baharkam Polri. Firli menyandang pangkat komisaris jenderal.</p>
			  <p>Dalam Pasal 29 UU KPK tersebut pimpinan KPK harus, poin (i) melepaskan jabatan struktural dan atau jabatan lainnya selama menjadi anggota KPK dan poin (j) tidak menjalankan profesinya selama menjadi anggota KPK.</p>
			  <p>Dini menyatakan ketentuan itu juga berlaku bagi Dewan Pengawas (Dewas) KPK. Menurutnya, anggota dewan pengawas yang memiliki jabatan lain sebelum resmi dilantik harus mengundurkan diri.</p>
			  <p>Diketahui anggota Dewas KPK yang memiliki jabatan lain antara lain Tumpak H. Panggabean sebagai komisaris utama PT Pelabuhan Indonesia II (Persero), Albertina Ho sebagai wakil ketua Pengadilan Tinggi Kupang, NTT.</p>
			  <p>Kemudian Harjono sebagai ketua Dewan Kehormatan Penyelenggara Pemilu (DKPP), dan Syamsuddin Haris sebagai peneliti senior pada Pusat Penelitian Politik (P2P) LIPI.</p>
			  <p>"Itu sudah clear di UU KPK bahwa Dewas tidak boleh rangkap jabatan. Jadi hrs mundur atau nonaktif dari jabatan lain," ujarnya.</p>
